# Define display_board that accepts a board and prints
# out the current state.
def display_board(choice)
  puts " #{choice[0]} | #{choice[1]} | #{choice[2]} "
  puts "-----------"
  puts " #{choice[3]}| #{choice[4]} | #{choice[5]} "
  puts "-----------"
  puts " #{choice[6]} | #{choice[7]} | #{choice[8]} "
end
